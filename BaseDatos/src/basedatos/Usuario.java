    public void CargarUsuarios(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);       
        try {
            while(res.next()){
                Vector v = new Vector();
                v.add(res.getInt(1));
                v.add(res.getString(2));
                v.add(res.getString(3));
                v.add(res.getString(4));
                v.add(res.getDate(5));
                modelo.addRow(v);
                jTable1.setModel(modelo);
            }
        } catch (Exception e) {
        }
    }


